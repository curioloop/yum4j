    public static double pValue(double statistic, TrendTerms trend, int seriesCount) {
        int tableIndex = trend.term();
        int row = seriesCount - 1;
        if (row < 0 || row >= TAU_MAX[tableIndex].length) {
            throw new IllegalArgumentException("MacKinnon p-value supports 1 to 6 series");
        }
        if (statistic > TAU_MAX[tableIndex][row]) return 1.0;
        if (statistic < TAU_MIN[tableIndex][row]) return 0.0;
        double[] coefficients = statistic <= TAU_STAR[tableIndex][row]
                ? TAU_SMALLP[tableIndex][row]
                : TAU_LARGEP[tableIndex][row];
        return new NormalDistribution().cdf(HornerPoly.evaluatePolynomial(coefficients, statistic));
    }

    public static Double3 criticalValues(int seriesCount, TrendTerms trend, int nobs) {
        return criticalValues(seriesCount, trend, (double) nobs);
    }

    public static Double3 criticalValues(int seriesCount, TrendTerms trend, double nobs) {
        double[][][] table = TAU_CRIT[trend.term()];
        if (seriesCount < 1 || seriesCount > table.length) {
            throw new IllegalArgumentException(
                    "MacKinnon critical values for regression='" + trend.code() + "' support 1 to " + table.length + " series");
        }
        double[][] coefficients = table[seriesCount - 1];
        if (Double.isInfinite(nobs)) {
            return new Double3(coefficients[0][0], coefficients[1][0], coefficients[2][0]);
        }
        if (!(nobs > 0.0) || Double.isNaN(nobs)) {
            throw new IllegalArgumentException("nobs must be positive or infinite");
        }
        double inv = 1.0 / nobs;
        return new Double3(
                HornerPoly.evaluatePolynomial(coefficients[0], inv),
                HornerPoly.evaluatePolynomial(coefficients[1], inv),
                HornerPoly.evaluatePolynomial(coefficients[2], inv));
    }

