    @Test
    void pacfBurgMatchesFacade() {
        // statsmodels: pacf_burg(realgdp, nlags=40) == pacf(realgdp, method='burg')
        // Both should return the same values
        PartialAutoCorrelation r1 = PartialAutoCorrelation.compute(
                realgdp, 40, PartialAutoCorrelation.Method.BURG, null);
        // Verify lag-0 is always 1
        assertEquals(1.0, r1.pacf()[0], TOL12);
        // Verify lag-1 is close to the YW estimate (both should be near 0.987)
        assertEquals(refPacfYw[0], r1.pacf()[1], 0.02, "pacf Burg lag1 near YW");
    }

    // ═══════════════════════════════════════════════════════════════════════════
    // 4. CrossCorrelation (CCF) — against results_ccf.csv
    //    Source: statsmodels/tsa/tests/test_stattools.py::TestCCF
    //    Input: unemp.diff() vs infl.diff() from macrodata
    // ═══════════════════════════════════════════════════════════════════════════

    @Test
    void ccfMatchesStata() {
        // statsmodels: ccf(unemp.diff, infl.diff, nlags=20, adjusted=False, fft=False)
        // == results_ccf.csv
        // Precision: DECIMAL_8
        double[] actual = CrossCorrelation.compute(
                unempDiff, inflDiff, false, false, 20, null).ccf();
        assertArrayEquals(refCcf, actual, TOL8, "ccf vs Stata");
    }

    @Test
    void ccfConfIntSymmetric() {
        // statsmodels: confint is symmetric around ccf values
        // alpha1 = norm.cdf(halfwidth / (1/sqrt(n))) should equal 1 - alpha/2
        double alpha = 0.05;
        CrossCorrelation r = CrossCorrelation.compute(
                unempDiff, inflDiff, false, false, 20, alpha);
        assertNotNull(r.confInt());
        double[] ccf = r.ccf();
        Double2[] ci = r.confInt();
        for (int i = 0; i < ccf.length; i++) {
            double lbDist = ccf[i] - ci[i].lower();
            double ubDist = ci[i].upper() - ccf[i];
            assertEquals(lbDist, ubDist, TOL8, "confint symmetric at lag=" + i);
        }
    }

    @Test
    void ccfFftMatchesDirect() {
        // FFT and direct paths should agree to 8 decimal places
        double[] direct = CrossCorrelation.compute(
                unempDiff, inflDiff, false, false, 20, null).ccf();
        double[] fft    = CrossCorrelation.compute(
                unempDiff, inflDiff, false, true,  20, null).ccf();
        assertArrayEquals(direct, fft, TOL8, "ccf fft vs direct");
    }

    @Test
    void correlateDirectModesMatchExpectedOverlapSemantics() {
        double[] a = {1.0, 2.0, 3.0, 4.0};
        double[] b = {5.0, 6.0, 7.0};

        assertArrayEquals(new double[] {38.0, 56.0},
                Correlate.correlate(a, b, Correlate.Mode.VALID, false), TOL12, "correlate valid");
        assertArrayEquals(new double[] {7.0, 20.0, 38.0, 56.0, 39.0, 20.0},
                Correlate.correlate(a, b, Correlate.Mode.FULL, false), TOL12, "correlate full");
        assertArrayEquals(new double[] {20.0, 38.0, 56.0, 39.0},
                Correlate.correlate(a, b, Correlate.Mode.SAME, false), TOL12, "correlate same");
    }

    @Test
    void correlateValidModePreservesShortLongOrientation() {
        double[] shortSeries = {5.0, 6.0, 7.0};
        double[] longSeries = {1.0, 2.0, 3.0, 4.0};

        assertArrayEquals(new double[] {56.0, 38.0},
                Correlate.correlate(shortSeries, longSeries, Correlate.Mode.VALID, false), TOL12,
                "correlate valid swapped");
    }

