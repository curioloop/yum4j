    /** Asserts element-wise equality with tolerance, printing the failing index. */
    private static void assertArrayEquals(double[] expected, double[] actual,
                                          double tol, String msg) {
        assertEquals(expected.length, actual.length, msg + " length");
        for (int i = 0; i < expected.length; i++) {
            assertEquals(expected[i], actual[i], tol, msg + "[" + i + "]");
        }
    }

    /** Returns {@code a[from..to-1]} as a new array. */
    private static double[] slice(double[] a, int from, int to) {
        double[] out = new double[to - from];
        System.arraycopy(a, from, out, 0, to - from);
        return out;
    }

    /** First-difference: {@code out[i] = a[i+1] - a[i]}. */
    private static double[] diff(double[] a) {
        double[] out = new double[a.length - 1];
        for (int i = 0; i < out.length; i++) out[i] = a[i + 1] - a[i];
        return out;
    }

