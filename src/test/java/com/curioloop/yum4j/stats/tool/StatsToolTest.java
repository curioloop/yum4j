    // ── Precision constants ───────────────────────────────────────────────────
    /** 8 decimal places — matches statsmodels DECIMAL_8 for ACF/PACF. */
    private static final double TOL8  = 1.5e-8;
    /** 6 decimal places — matches statsmodels DECIMAL_6 for PACF OLS. */
    private static final double TOL6  = 1.5e-6;
    /** 3 decimal places — matches statsmodels DECIMAL_3 for Q-statistics. */
    private static final double TOL3  = 1.5e-3;
    /** 12 decimal places — matches statsmodels decimal=(12,12) for diagnostics. */
    private static final double TOL12 = 1.5e-12;

    // ── Shared data loaded once ───────────────────────────────────────────────
    private static double[] realgdp;
    private static double[] unempDiff;
    private static double[] inflDiff;

    // ── Reference fixture arrays ──────────────────────────────────────────────
    private static double[] refAcf;        // results_corrgram.csv "acvar"  (40 values)
    private static double[] refAcfFft;     // results_corrgram.csv "acvarfft"
